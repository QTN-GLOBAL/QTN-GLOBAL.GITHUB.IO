/* =========================
   GET TRANSLATED NEWS
========================= */

function getTranslatedNews(news){

    const lang =
        localStorage.getItem("language") || "vi";

    const trans =
        newsTranslations?.[lang]?.[news.id];

    if(!trans){
        return news;
    }

    return {
        ...news,
        title: trans.title,
        summary: trans.summary,
        content: trans.content
    };
}